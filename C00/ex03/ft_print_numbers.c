#include <unistd.h>

void	ft_print_numbers(void)
{
	int	cpt;
	int	a;

	a = '0';
	cpt = 0;
	while (cpt <= 9)
	{
		write(1, &a, 1);
		a++;
		cpt++;
	}
}
